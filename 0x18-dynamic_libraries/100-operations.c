#include <stdio.h>

/**
 * sum - add numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - add numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: sum
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * div - add numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: sum
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mul - add numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: sum
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * mod - add numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: sum
 */
int mod(int a, int b)
{
        return (a % b);
}
